#include "../push_swap.h"

t_list	*ft_lstnew(int content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	new->value = content;
	new->next = NULL;
	new->prev = NULL;
	return (new);
}
